package hr.fer.zemris.java.gui.calc.buttons;

import hr.fer.zemris.java.gui.calc.Calculator;

/**
 * Enumeration that specifies what types of buttons are used in
 * {@link Calculator}.
 * 
 * @author Nikola Bukovac
 * @version 1.0
 */
public enum ButtonType {

	/** Digit button */
	DIGIT,

	/** Control button */
	CONTROL,

	/** Unary operation button */
	UNARY,

	/** Binary operation button */
	BINARY,

	/** Stack operation button */
	STACK
}
